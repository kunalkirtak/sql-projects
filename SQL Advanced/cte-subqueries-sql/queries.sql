------------------------------------------------------------
-- 36. EXISTS with Correlated Subquery
-- Customers who bought more than one distinct product
------------------------------------------------------------

SELECT
    c.customer_name
FROM customers c
WHERE EXISTS
(
    SELECT 1
    FROM orders o
    WHERE o.customer_id = c.customer_id
    GROUP BY o.customer_id
    HAVING COUNT(DISTINCT o.product_id) > 1
)
ORDER BY c.customer_name;



------------------------------------------------------------
-- 37. EXISTS
-- Products ordered in quantity greater than 2
------------------------------------------------------------

SELECT
    p.product_name
FROM products p
WHERE EXISTS
(
    SELECT 1
    FROM orders o
    WHERE o.product_id = p.product_id
      AND o.quantity > 2
)
ORDER BY p.product_name;



------------------------------------------------------------
-- 38. Combined Example
-- Customers who purchased products priced above
-- the average product price
------------------------------------------------------------

SELECT
    customer_name
FROM customers
WHERE customer_id IN
(
    SELECT DISTINCT o.customer_id
    FROM orders o
    JOIN products p
        ON o.product_id = p.product_id
    WHERE p.price >
    (
        SELECT AVG(price)
        FROM products
    )
)
ORDER BY customer_name;