------------------------------------------------------------
-- 4. Single CTE
-- Calculate total amount for every order
------------------------------------------------------------

WITH order_total AS
(
    SELECT
        o.order_id,
        p.product_name,
        o.quantity,
        p.price,
        (o.quantity * p.price) AS total_amount
    FROM orders o
    JOIN products p
        ON o.product_id = p.product_id
)

SELECT *
FROM order_total
ORDER BY total_amount DESC;



------------------------------------------------------------
-- 5. Multiple CTEs
-- Expensive products + Customer Orders
------------------------------------------------------------

WITH expensive_products AS
(
    SELECT
        product_id,
        product_name,
        price
    FROM products
    WHERE price > 10000
),

customer_orders AS
(
    SELECT
        customer_id,
        product_id,
        quantity
    FROM orders
)

SELECT
    c.customer_id,
    c.product_id,
    e.product_name,
    e.price,
    c.quantity
FROM customer_orders c
JOIN expensive_products e
ON c.product_id = e.product_id
ORDER BY e.price DESC;



------------------------------------------------------------
-- 6. Multiple CTEs
-- Total Orders per Customer
------------------------------------------------------------

WITH customer_orders AS
(
    SELECT
        customer_id,
        COUNT(*) AS total_orders
    FROM orders
    GROUP BY customer_id
),

customer_information AS
(
    SELECT
        customer_id,
        customer_name,
        city
    FROM customers
)

SELECT
    ci.customer_name,
    ci.city,
    co.total_orders
FROM customer_information ci
JOIN customer_orders co
ON ci.customer_id = co.customer_id
ORDER BY co.total_orders DESC;



------------------------------------------------------------
-- 7. Multiple CTEs
-- Revenue generated by every product
------------------------------------------------------------

WITH sales_data AS
(
    SELECT
        product_id,
        SUM(quantity) AS total_quantity
    FROM orders
    GROUP BY product_id
),

product_revenue AS
(
    SELECT
        p.product_name,
        s.total_quantity,
        p.price,
        (s.total_quantity * p.price) AS revenue
    FROM products p
    JOIN sales_data s
    ON p.product_id = s.product_id
)

SELECT *
FROM product_revenue
ORDER BY revenue DESC;



------------------------------------------------------------
-- 8. Chained CTEs (Nested Logic)
-- Find customers who spent more than ₹50,000
------------------------------------------------------------

WITH order_amount AS
(
    SELECT
        o.customer_id,
        (o.quantity * p.price) AS amount
    FROM orders o
    JOIN products p
    ON o.product_id = p.product_id
),

customer_total AS
(
    SELECT
        customer_id,
        SUM(amount) AS total_spent
    FROM order_amount
    GROUP BY customer_id
)

SELECT
    c.customer_name,
    ct.total_spent
FROM customer_total ct
JOIN customers c
ON ct.customer_id = c.customer_id
WHERE ct.total_spent > 50000
ORDER BY ct.total_spent DESC;



------------------------------------------------------------
-- 9. Chained CTEs (Nested Logic)
-- Products sold more than twice
------------------------------------------------------------

WITH product_sales AS
(
    SELECT
        product_id,
        SUM(quantity) AS total_quantity
    FROM orders
    GROUP BY product_id
),

popular_products AS
(
    SELECT
        *
    FROM product_sales
    WHERE total_quantity > 2
)

SELECT
    p.product_name,
    pp.total_quantity
FROM popular_products pp
JOIN products p
ON pp.product_id = p.product_id
ORDER BY pp.total_quantity DESC;



------------------------------------------------------------
-- 10. Chained CTEs (Nested Logic)
-- Average order value for every customer
------------------------------------------------------------

WITH order_values AS
(
    SELECT
        o.customer_id,
        (o.quantity * p.price) AS order_value
    FROM orders o
    JOIN products p
    ON o.product_id = p.product_id
),

customer_average AS
(
    SELECT
        customer_id,
        AVG(order_value) AS average_order_value
    FROM order_values
    GROUP BY customer_id
)

SELECT
    c.customer_name,
    ROUND(ca.average_order_value,2) AS average_order_value
FROM customer_average ca
JOIN customers c
ON ca.customer_id = c.customer_id
ORDER BY average_order_value DESC;

------------------------------------------------------------
-- 11. Recursive CTE
-- Generate numbers from 1 to 10
------------------------------------------------------------

WITH RECURSIVE numbers AS
(
    SELECT 1 AS number

    UNION ALL

    SELECT number + 1
    FROM numbers
    WHERE number < 10
)

SELECT *
FROM numbers;



------------------------------------------------------------
-- 12. Recursive CTE
-- Generate the first 12 months
------------------------------------------------------------

WITH RECURSIVE months AS
(
    SELECT
        1 AS month_number

    UNION ALL

    SELECT
        month_number + 1
    FROM months
    WHERE month_number < 12
)

SELECT *
FROM months;



------------------------------------------------------------
-- 13. Recursive CTE
-- Generate dates for one week
------------------------------------------------------------

WITH RECURSIVE dates AS
(
    SELECT
        DATE '2025-01-01' AS order_day

    UNION ALL

    SELECT
        order_day + INTERVAL '1 day'
    FROM dates
    WHERE order_day < DATE '2025-01-07'
)

SELECT order_day
FROM dates;



