------------------------------------------------------------
-- 1. Single CTE
-- Display all products costing more than ₹10,000
------------------------------------------------------------

WITH expensive_products AS
(
    SELECT
        product_id,
        product_name,
        category,
        price
    FROM products
    WHERE price > 10000
)

SELECT *
FROM expensive_products;



