------------------------------------------------------------
-- 20. Correlated Subquery
-- Customer's latest order
------------------------------------------------------------

SELECT
    c.customer_name,
    o.order_id,
    o.order_date
FROM customers c
JOIN orders o
ON c.customer_id = o.customer_id
WHERE o.order_date =
(
    SELECT MAX(o2.order_date)
    FROM orders o2
    WHERE o2.customer_id = c.customer_id
)
ORDER BY c.customer_name;



