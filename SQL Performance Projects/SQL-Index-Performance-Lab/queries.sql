---------------------------------------------------------
-- View First 10 Products
---------------------------------------------------------

SELECT *
FROM products
LIMIT 10;

---------------------------------------------------------
-- Find Electronics
---------------------------------------------------------

SELECT *
FROM products
WHERE category='Electronics';

---------------------------------------------------------
-- Products Above ₹7000
---------------------------------------------------------

SELECT *
FROM products
WHERE price>7000;

---------------------------------------------------------
-- Products Between ₹2000 and ₹4000
---------------------------------------------------------

SELECT *
FROM products
WHERE price BETWEEN 2000 AND 4000;

---------------------------------------------------------
-- Order by Price
---------------------------------------------------------

SELECT *
FROM products
ORDER BY price DESC
LIMIT 20;

---------------------------------------------------------
-- Exact Product Search
---------------------------------------------------------

SELECT *
FROM products
WHERE product_name='Product-50000';