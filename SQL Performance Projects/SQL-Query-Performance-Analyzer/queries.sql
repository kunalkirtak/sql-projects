--------------------------------------------------------
-- Count Employees
--------------------------------------------------------

SELECT COUNT(*)
FROM employees;

--------------------------------------------------------
-- AI Department
--------------------------------------------------------

SELECT *
FROM employees
WHERE department='AI';

--------------------------------------------------------
-- Salary Above 100000
--------------------------------------------------------

SELECT *
FROM employees
WHERE salary>100000;

--------------------------------------------------------
-- Employees From Pune
--------------------------------------------------------

SELECT *
FROM employees
WHERE city='Pune';

--------------------------------------------------------
-- Top 10 Highest Salary
--------------------------------------------------------

SELECT *
FROM employees
ORDER BY salary DESC
LIMIT 10;